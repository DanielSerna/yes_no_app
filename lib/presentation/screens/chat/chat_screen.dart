class ChatScreen extends StatelessWidget {
  const ChatScreen({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        leading: const Padding(
          padding: EdgeInsets.all(4.0),
          child: CircleAvatar(
            backgroundImage: NetworkImage(
                'https://www.eluniversal.com.mx/resizer/v2/TQZVLKIYMNE6XLDHE57ANFBY2M.jpg?auth=62523be04b48a0e801fd2c15d14ff3918b84b5986db7d6c85f934ef3e40fe333&smart=true&width=1100&height=666'),
          ),
        ),
        title: const Text('Mi amor'),
        centerTitle: false,
      ),
      body: _ChatView(),
    );
  }
}

class _ChatView extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    final chatProvider = context.watch<ChatProvider>();

    return SafeArea(
      child: Padding(
        padding: const EdgeInsets.symmetric(horizontal: 10),
        child: Column(
          children: [
            Expanded(
                child: ListView.builder(
                  controller: chatProvider.chatScrollCrontoller,
                    itemCount: chatProvider.messageList.length,
                    itemBuilder: (context, index) {
                      final message = chatProvider.messageList[index];
                      return (message.fromWho == FromWho.hers)
                          ? HerMessageBubble( herMessage: message,)
                          : MyMessageBubble(message: message);
                    })),
            MessageFieldBox( onValue:chatProvider.sendMessage)
          ],
        ),
      ),
    );
  }
}
